package br.com.codersistemas.gem.exceptions;

public class TemplateGemException extends GemException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public TemplateGemException() {
		super();
	}

	public TemplateGemException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TemplateGemException(String message, Throwable cause) {
		super(message, cause);
	}

	public TemplateGemException(String message) {
		super(message);
	}

	public TemplateGemException(Throwable cause) {
		super(cause);
	}

}
